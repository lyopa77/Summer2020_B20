package day12_switch;

public class Switch {
    public static void main(String[] args) {
        switch (6){
            case 5:
                System.out.println(5); //five
                break;

            case 1:
                System.out.println(1);//"one"
                break;
            default:
                System.out.println("invalid");
break;



        }

    }
}
