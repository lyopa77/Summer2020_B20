package day22_nestedLoop;

public class nested_loop {
    public static void main(String[] args) {
        for (int i =1; i<=5;i++) {
            System.out.print(i + " ");
        }
        System.out.println();
int j = 1;
        while (j<=5) {
            System.out.print(j + " ");
            j++;
    }
        System.out.println();
        System.out.println("=======");
}}
