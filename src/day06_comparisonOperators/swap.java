package day06_comparisonOperators;

public class swap {
    public static void main(String[] args) {
        int a = 10;
        int b = 15;

        int c = b; //c = 15
        b = a; //b=10
        //a=15
        a = c;
        System.out.println("a " + a);
        System.out.println("b " + b);




    }



}
