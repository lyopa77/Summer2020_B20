package officeHRS;

public class switch_quiz {
    public static void main(String[] args) {
        int a =11;
        switch (a){
            case 10:
                System.out.println("monday");
                break;
            case 11:
                System.out.println("tuesday");
                break;
            case 12:
                System.out.println("wed");
                break;
            default:
                System.out.println("friday");



        }
    }

}
