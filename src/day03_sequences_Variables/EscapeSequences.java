package day03_sequences_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello, \n" +
                "My name is A. ");
        System.out.println("\tI am in PGH");
        System.out.println("\n\n\t\t\tI love programming");
        System.out.println("\\");//prints 1 backslash
        System.out.println("/");//prints one forward slash

        System.out.println("\\\\");//prints 2 backslash
        System.out.println("//");//prints 2 front slash

        System.out.println("My name is \'Alexey\'");
        System.out.println("My favorite tv series: \"Game of Thrones\"");




    }


}
